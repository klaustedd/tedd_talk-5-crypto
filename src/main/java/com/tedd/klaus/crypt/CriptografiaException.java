package com.tedd.klaus.crypt;

public class CriptografiaException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CriptografiaException() {
		super();
	}

	public CriptografiaException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CriptografiaException(String message, Throwable cause) {
		super(message, cause);
	}

	public CriptografiaException(String message) {
		super(message);
	}

	public CriptografiaException(Throwable cause) {
		super(cause);
	}
	
	

}
